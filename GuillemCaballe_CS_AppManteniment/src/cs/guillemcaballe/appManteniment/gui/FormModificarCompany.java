    public void setCompany(Company company)    {
        this.company = company;
        this.companyAux = company;
        configuracio();
    }
    
    private void configuracio() {
        if (company != null) {
            campUsuari.setText(company.getUsuari().getUsername());
            campDni.setText(company.getCif());
            campNom.setText(company.getName());
            campAddress.setText(company.getAddress());
            campCorreu.setText(company.getUsuari().getCorreu());
        }
        
        this.setVisible(false);
    }
    
    private void prepararEvents()   {
        btnCancelar.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                FormModificarCompany.this.dispose();
            }
        });
        btnModificar.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                company.getUsuari().setUsername(campUsuari.getText());
                company.setCif(campDni.getText());
                company.setName(campNom.getText());
                company.setAddress(campAddress.getText());
                company.getUsuari().setCorreu(campCorreu.getText());
                
                try {
                    company.update();
                    company.getUsuari().update();
                    FormManteniment.removeCompany(companyAux);
                    FormManteniment.addCompany(company);
                    /*if (companyAux.equals(Controlador.getUsuariConnectat()))    {
                        Controlador.setUsuariConnectat(usuari);
                    }*/
                    FormManteniment.hiHanCanvis();
                    JOptionPane.showMessageDialog(FormModificarCompany.this, "Company modificada correctament!", "Correcte", JOptionPane.INFORMATION_MESSAGE);
                    FormModificarCompany.this.dispose();
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(FormModificarCompany.this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }
        });
    }

